using UnityEngine;

public class DaySystem : MonoBehaviour
{
    public SpriteRenderer dayDesert;
    public SpriteRenderer daySky;

    public float cycleDuration = 120f; // ?????? ????: ???? ? ???? ? ????
    public float alpha = 1; 

    void Update()
    {
        // ???????? ????? ? ???????? ?????? ??????? ?????
        float time = Time.time % cycleDuration;

        // ??????????? ????? ? ???????? ?? 0 ?? 1, ????? ?????????? ????? ??? ???????? ????????
        float t = time / cycleDuration;
        alpha = Mathf.Cos(t * 2 * Mathf.PI) * 0.5f + 0.5f; // ????????? ?? 1 ?? 0 ? ???????

        SetAlpha(dayDesert, alpha);
        SetAlpha(daySky, alpha);
    }

    void SetAlpha(SpriteRenderer renderer, float alpha)
    {
        Color color = renderer.color;
        color.a = alpha;
        renderer.color = color;
    }
}